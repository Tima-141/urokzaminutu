export interface GeneratedLesson {
  topic: string;
  grade: string;
  level: string;
  duration: string;
  goal: string;
  explanation: string;
  plan: { time: string; activity: string }[];
  tasks: { number: number; text: string; answer: string; difficulty: string }[];
  homework: { number: number; text: string; answer: string }[];
  test: { number: number; question: string; options: string[]; correct: string }[];
  parentReport: string;
}

export function getMockResult(topic: string, grade: string, level: string, duration: string, goal: string): GeneratedLesson {
  return {
    topic: topic || 'Линейные уравнения',
    grade,
    level,
    duration,
    goal: getGoalText(goal, topic, level),
    explanation: getExplanation(topic, level),
    plan: getPlan(duration, level),
    tasks: getTasks(level),
    homework: getHomework(level),
    test: getTest(level),
    parentReport: getParentReport(topic, grade, level),
  };
}

function getGoalText(goal: string, topic: string, level: string): string {
  const levelNote = level === 'Слабый' 
    ? ' Особое внимание на базовые понятия и пошаговое объяснение.'
    : level === 'Сильный'
    ? ' Акцент на сложные случаи и нестандартные задачи.'
    : '';
    
  const goals: Record<string, string> = {
    'new': `Объяснить новую тему «${topic || 'Линейные уравнения'}» — дать понятное объяснение, разобрать примеры и закрепить через практику.${levelNote}`,
    'practice': `Отработать на практике тему «${topic || 'Линейные уравнения'}» — решить задания от простого к сложному и выявить пробелы.${levelNote}`,
    'test-prep': `Подготовить ученика к контрольной работе по теме «${topic || 'Линейные уравнения'}» — повторить теорию и решить типовые задания.${levelNote}`,
    'oge': `Подготовить ученика к ОГЭ по теме «${topic || 'Линейные уравнения'}» — разобрать задания в формате экзамена.${levelNote}`,
  };
  return goals[goal] || goals['new'];
}

function getExplanation(topic: string, level: string): string {
  if (topic.toLowerCase().includes('квадрат')) {
    if (level === 'Слабый') {
      return `Квадратное уравнение — это уравнение, где x умножается сам на себя (x²).\n\nПростой пример: x² = 9. Какое число, умноженное само на себя, даёт 9? Правильно, 3! (потому что 3 × 3 = 9). Но есть ещё -3 (потому что -3 × -3 тоже = 9).\n\nОбщий вид: ax² + bx + c = 0\n\nГде:\n• a, b, c — это числа (коэффициенты)\n• x — то, что мы ищем\n• a не может быть равно 0\n\nДля решения используем формулу дискриминанта:\nD = b² − 4ac\n\nЗатем находим x по формуле:\nx = (−b ± √D) / (2a)\n\nНе волнуйся, мы разберём это пошагово на примерах!`;
    }
    if (level === 'Сильный') {
      return `Квадратное уравнение ax² + bx + c = 0 (a ≠ 0) — фундаментальный объект алгебры.\n\nМетоды решения:\n1. Через дискриминант: D = b² − 4ac, x₁,₂ = (−b ± √D) / (2a)\n2. Теорема Виета: x₁ + x₂ = −b/a, x₁ · x₂ = c/a\n3. Выделение полного квадрата\n4. Разложение на множители\n\nОсобые случаи:\n• Неполные уравнения (b=0 или c=0) — решаются проще\n• Биквадратные уравнения (ax⁴ + bx² + c = 0) — замена t = x²\n• Уравнения с параметром\n\nИсследование корней:\n• D > 0 → два различных корня\n• D = 0 → один корень (кратности 2)\n• D < 0 → нет действительных корней (есть комплексные)\n\nГрафическая интерпретация: корни — точки пересечения параболы y = ax² + bx + c с осью Ox.`;
    }
    return `Квадратное уравнение — это уравнение вида ax² + bx + c = 0, где a ≠ 0.\n\nПредставь, что ты бросаешь мяч вверх. Путь мяча описывается именно квадратным уравнением! Мяч поднимается, достигает максимальной высоты и падает вниз — это и есть парабола.\n\nЧтобы решить квадратное уравнение, используем дискриминант:\nD = b² − 4ac\n\nЕсли D > 0 — два корня\nЕсли D = 0 — один корень\nЕсли D < 0 — нет корней\n\nФормула корней: x = (−b ± √D) / (2a)\n\nТеорема Виета (полезно для проверки):\n• Сумма корней: x₁ + x₂ = −b/a\n• Произведение корней: x₁ · x₂ = c/a`;
  }
  
  // Линейные уравнения
  if (level === 'Слабый') {
    return `Линейное уравнение — это уравнение с буквой x (неизвестным).\n\nПростой пример: x + 5 = 8\nВопрос: какое число прибавить к 5, чтобы получить 8?\nОтвет: 3! Значит x = 3.\n\nПредставь весы ⚖️:\n• Слева: x + 5\n• Справа: 8\n• Весы в равновесии!\n\nЧтобы найти x, нужно \"убрать\" пятёрку слева. Как? Вычесть 5 с ОБЕИХ сторон:\nx + 5 − 5 = 8 − 5\nx = 3\n\nГлавное правило: что делаем слева, то же делаем справа!\n\nАлгоритм:\n1. Всё с x — влево\n2. Числа — вправо\n3. При переносе меняем знак!\n4. Находим x`;
  }
  if (level === 'Сильный') {
    return `Линейное уравнение ax + b = 0 (a ≠ 0) имеет единственный корень x = −b/a.\n\nОбщий вид: ax + b = cx + d\nРешение: (a−c)x = d−b, x = (d−b)/(a−c) при a ≠ c\n\nОсобые случаи:\n• a = c, b ≠ d → нет решений (параллельные прямые)\n• a = c, b = d → бесконечно много решений (совпадающие прямые)\n\nУсложнённые типы:\n1. С модулем: |ax + b| = c → два случая\n2. С параметром: ax = b, исследование при разных a\n3. Дробно-рациональные: (ax+b)/(cx+d) = e, ОДЗ!\n4. Системы линейных уравнений\n\nМетоды решения систем:\n• Подстановка\n• Сложение\n• Графический\n• Метод Крамера (определители)\n\nГеометрический смысл: решение — точка пересечения прямых y = ax + b и y = c.`;
  }
  return `Линейное уравнение — это уравнение вида ax + b = c, где x — неизвестное.\n\nПредставь, что уравнение — это весы. Слева и справа от знака «равно» стоят чаши весов. Чтобы весы оставались в равновесии, нужно делать одинаковые действия с обеими сторонами.\n\nНапример, если к обеим чашам добавить одинаковый груз или убрать — весы останутся в равновесии. Точно так же и с уравнением: можно прибавлять, вычитать, умножать или делить обе части на одно и то же число.\n\nАлгоритм решения:\n1. Перенесите все слагаемые с x в левую часть\n2. Перенесите числа в правую часть (меняя знак!)\n3. Приведите подобные слагаемые\n4. Разделите обе части на коэффициент при x\n\nПроверка: подставьте найденное значение x в исходное уравнение.`;
}

